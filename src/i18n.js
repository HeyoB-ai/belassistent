// --- Context & hook -------------------------------------------------------

const LangContext = createContext(null);

// Provider zonder JSX (dit is een .js-bestand) via createElement.
export function LangProvider({ children }) {
  const [lang, setLang] = useState(null); // null = nog geen taal gekozen
  return createElement(LangContext.Provider, { value: { lang, setLang } }, children);
}

export function useLang() {
  const ctx = useContext(LangContext);
  if (!ctx) throw new Error('useLang moet binnen <LangProvider> gebruikt worden.');
  const { lang, setLang } = ctx;
  const t = TRANSLATIONS[lang] || TRANSLATIONS.en;
  const isRtl = Boolean(LANGUAGES.find((l) => l.code === lang)?.rtl);
  return { lang, setLang, t, isRtl };
}
